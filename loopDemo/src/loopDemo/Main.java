package loopDemo;

import java.util.Iterator;

public class Main {

    public static void main(String[] args) {
        //For
        
        for (int i = 1; i <= 10; i+=2) {
            System.out.println(i);
        }
        System.out.println("For Dongusu bitti");
        System.out.println();
        System.out.println();
        System.out.println();
        
        
        //while
        
        int i=1;
        while (i<10) {
            System.out.println(i);
            i++;
        }
        System.out.println("While Dongusu bitti");
        
        
    }

}
